(function($){
	var initLayout = function() {
		$('.colorSelector').ColorPicker({
			color: '#252525',
			onShow: function (colpkr) {
				$(colpkr).fadeIn(500);
				return false;
			},
			onHide: function (colpkr) {
				$(colpkr).fadeOut(500);
				return false;
			},
			onChange: function (hsb, hex, rgb) {
				$('.colorSelector div, #color-picker-container').css('backgroundColor', '#' + hex);
			}
		});
		$('.colorSelector2').ColorPicker({
			color: '#b62c2c',
			onShow: function (colpkr) {
				$(colpkr).fadeIn(500);
				return false;
			},
			onHide: function (colpkr) {
				$(colpkr).fadeOut(500);
				return false;
			},
			onChange: function (hsb, hex, rgb) {
				$('#main-container a').css('color', '#' + hex);
				$('.colorSelector2 div, a.show-case-button').css('backgroundColor', '#' + hex);
			}
		});
		$('.colorSelector3').ColorPicker({
			color: '#5a5959',
			onShow: function (colpkr) {
				$(colpkr).fadeIn(500);
				return false;
			},
			onHide: function (colpkr) {
				$(colpkr).fadeOut(500);
				return false;
			},
			onChange: function (hsb, hex, rgb) {
				$('.colorSelector3 div, #footer-container, body#footer-contact').css('backgroundColor', '#' + hex);
			}
		});
	};
	
	var showTab = function(e) {
		var tabIndex = $('ul.navigationTabs a')
							.removeClass('active')
							.index(this);
		$(this)
			.addClass('active')
			.blur();
		$('div.tab')
			.hide()
				.eq(tabIndex)
				.show();
	};
	
	EYE.register(initLayout, 'init');
})(jQuery)